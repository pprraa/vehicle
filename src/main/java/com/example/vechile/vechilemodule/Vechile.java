package com.example.vechile.vechilemodule;

public class Vechile {
    private String vechilemode;
    private String vechilebrand;
    private String vechilecolour;
    private String vechilepower;

    public String getVechilemode() {
        return vechilemode;
    }

    public void setVechilemode(String vechilemode) {
        this.vechilemode = vechilemode;
    }

    public String getVechilebrand() {
        return vechilebrand;
    }

    public void setVechilebrand(String vechilebrand) {
        this.vechilebrand = vechilebrand;
    }

    public String getVechilecolour() {
        return vechilecolour;
    }

    public void setVechilecolour(String vechilecolour) {
        this.vechilecolour = vechilecolour;
    }

    public String getVechilepower() {
        return vechilepower;
    }

    public void setVechilepower(String vechilepower) {
        this.vechilepower = vechilepower;
    }
}
